int main() {
    float output[4];
    long long total_nsec=0;
    struct timespec hopper1 , hopper2;
    // Column headers
    printf("%-8s %9s %9s %9s %9s\n", "Sample", "Class 0", "Class 1", "Class 2", "Class 3");
    printf("------------------------------------------------------------\n");
    for (int i = 0; i < INPUTS; i++) {

        clock_gettime(CLOCK_MONOTONIC_RAW, &hopper1);
        predict(test_data[i], output);
        clock_gettime(CLOCK_MONOTONIC_RAW, &hopper2);

        // Convert times to nanoseconds and accumulate difference
        long long start_ns = (long long)hopper1.tv_sec * 1000000000LL + hopper1.tv_nsec;
        long long end_ns = (long long)hopper2.tv_sec * 1000000000LL + hopper2.tv_nsec;
        total_nsec += (end_ns - start_ns);

        // Find the max value
        float max = output[0];
        for (int j = 1; j < 4; j++) {
            if (output[j] > max) max = output[j];
        }
        // Row output
        printf("%-8d", i);
        for (int j = 0; j < 4; j++) {
            if (output[j] == max)
                printf(GREEN"%10.6f"RESET, output[j]);
            else
                printf("%10.6f", output[j]);
        }
        printf("\n\r");
    }
    printf("Elapsed time (%d Predictions): %lld nsec\n", INPUTS, total_nsec);
    printf("Meaning %lld nsec/prediction\n", total_nsec / INPUTS);
    return 0;
}












